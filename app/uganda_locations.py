def get_all_districts():
    """Get all districts sorted alphabetically"""
    return sorted(UGANDA_LOCATIONS.keys())

def get_subcounties(district):
    """Get subcounties for a specific district"""
    if district in UGANDA_LOCATIONS:
        return sorted(UGANDA_LOCATIONS[district]["subcounties"])
    return []

def get_villages(district):
    """Get villages/parishes for a specific district"""
    if district in UGANDA_LOCATIONS:
        return sorted(UGANDA_LOCATIONS[district]["villages"])
    return []

def get_district_choices():
    """Get district choices for form SelectField - sorted alphabetically"""
    districts = get_all_districts()
    return [('', 'Select District')] + [(d.lower().replace(' ', '_').replace('-', '_'), d) for d in districts]
